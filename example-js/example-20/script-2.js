// –ê –¥–∞–ª—å—à–µ —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ .includes() –ø—Ä–æ–≤–µ—Ä–∏—Ç—å, –µ—Å—Ç—å –ª–∏ —Ç–∞–∫–æ–π –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä –≤ –º–∞—Å—Å–∏–≤–µ –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–≤ modifiers. –ï—Å–ª–∏ –Ω–µ—Ç, —Ç–æ –ø—É–Ω–∫—Ç —Å–ø–∏—Å–∫–∞ –º–æ–∂–Ω–æ —É–¥–∞–ª—è—Ç—å –∏–∑ —Ä–∞–∑–º–µ—Ç–∫–∏, –∑–∞–¥–∞—á–∞ —Ä–µ—à–µ–Ω–∞.


// –ø–µ—Ä–µ–±–µ—Ä–µ–º —Å–ø–∏—Å–æ–∫ —Å —ç–∏–æ—Ü–∏—è–º–∏ –∏–∑ html
emojiList.forEach((emojiListItem) => {
  // –¥–ª—è –∫–∞–∂–¥–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞ emojiList —Å–æ–∑–¥–∞–¥–∏–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç —Ö—Ä–∞–Ω–∏—Ç—å –µ–≥–æ –≤—Ç–æ—Ä–æ–π –∫–ª–∞—Å—Å –ø–æ–ª—É—á–µ–Ω–Ω—ã–π —Å –ø–æ–º–æ—â—å classList[1] (–Ω—É–ª–µ–≤–æ–π –∏–Ω–¥–µ–∫—Å –µ—Å—Ç–µ—Å—Ç–≤–µ–Ω–Ω–æ –¥–ª—è –ø–µ—Ä–≤–æ–≥–æ –∫–ª–∞—Å—Å–∞, –Ω–æ –æ–Ω –Ω–∞–º –Ω–µ –Ω—É–∂–µ–Ω) (–º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä –ø–æ –ë–≠–ú)

  const modifier = emojiListItem.classList[1];
  // 1 - —ç—Ç–æ –∏–Ω–¥–µ–∫—Å –Ω—É–∂–Ω–æ–≥–æ –∫–ª–∞—Å—Å–∞ –≤ –∞—Ç—Ä–∏–±—É—Ç–µ class

  // –ø—Ä–∏–º–µ–Ω–∏–º —É—Å–ª–æ–≤–∏—è —á—Ç–æ–±—ã —É–¥–∞–ª–∏—Ç—å —Ç–µ —ç–ª–µ–º–µ–Ω—Ç—ã –∏–∑ emojiList –¥–ª—è –∫–æ—Ç–æ—Ä—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π modifier –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç—Å—è –≤ –º–æ–¥–∏—Ñ–∏—Ü–∏—Ä–æ–≤–∞–Ω–Ω–æ–º –º–∞—Å—Å–∏–≤–µ —Å –¥–∞–Ω–Ω—ã–º–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–≤ –º–∞—Å–∏–≤–µ modifiers)

  if (!modifiers.includes(modifier)) {
    emojiListItem.remove();
  }
});



